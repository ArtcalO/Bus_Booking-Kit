    private void affiche(){
        try{
        dt.setRowCount(0);
        ResultSet request=Connexion.interrogerBD("select * from Bus");
        while(request.next()){
            
            id=request.getInt("Id_Bus");
            Imatriculation=request.getString("Imatriculation");
            Mark=request.getString("Mark");
            seats=request.getInt("Seat");
            
            Object [] el={id,Imatriculation,Mark,seats};
            dt.addRow(el);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

